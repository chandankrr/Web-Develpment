// boolean & comparision operator
let num1 = 20;
let num2 = "20";

// == vs ===
console.log(num1 == num2); // ture -> value is equal
console.log(num1 === num2); // false -> data type is not equal

// != vs !==
console.log(num1 != num2); // false -> value is equal
console.log(num1 !== num2); // true -> data type is not equal
